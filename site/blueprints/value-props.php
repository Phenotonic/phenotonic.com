      #### How to Create a Value Prop

      1. Add a page by clicking on the 'Add' button of the 'Pages' section in the sidebar.

      2. Name the page after the value discussed in the value prop. Choose the 'value-prop' template (it should be the only one available).

      3. Click on the name of the page you just created in the 'Pages' section in the sidebar.

      4. Follow the instructions on that page.

      5. If you can't add any more value props, it's likely you've added the maximum 5 allowed.


      #### How to Re-order Value Props

      Value props are shown in order on the. To change the order in which they are shown, simply rearrange them in the 'Pages' section of the sidebar.

